<template>
    <router-link v-if="false !== to"
                 :to="to"
                 class="btn"
                 :class="modifierClass"
                 @click="clickBtn()"
    >{{title}}
    </router-link>
    <a
            v-else
            class="btn"
            :class="modifierClass"
            @click="clickBtn()">
        {{title}}
    </a>
</template>

<script>
    export default {
        name: "Btn",
        props: {
            'title': String,
            'modifier': null,
            'to': {
                default: false
            }
        },
        computed: {
            modifierClass() {
                return this.modifier ? ('btn--' + this.modifier) : ''
            }
        },
        methods: {
            clickBtn() {
                this.$emit('click-btn');
            }
        }
    }
</script>

